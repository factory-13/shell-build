# -------------------------------------------------------------------------------------------------------------------- #
# Build SRPM.
# -------------------------------------------------------------------------------------------------------------------- #

ext.mock.build.srpm() {
    config="${1}"
    package="${2}"

    mock -r ${config}               \
    --spec=specs/${package}.spec    \
    --sources=sources/${package}    \
    --resultdir=srpms               \
    --buildsrpm
}

# -------------------------------------------------------------------------------------------------------------------- #
# Build RPM.
# -------------------------------------------------------------------------------------------------------------------- #

ext.mock.build.rpm() {
    config="${1}"
    package="${2}"

    mock -r ${config}   \
    --resultdir=rpms    \
    --rebuild srpms/${package}.src.rpm
}
